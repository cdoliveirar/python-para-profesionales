'''
if condition:
    body

Las declaraciones if verifican la condición. Si se evalúa como Verdadero, ejecuta el cuerpo de la declaración if.
Si se evalúa como Falso, omite el cuerpo(body)
'''

if True:
    print("Esto es verdad!")

if False:
    print("Esto no sera impreso..")


#  La condición puede ser cualquier expresión válida.
if 2 + 2 == 4:
    print("sé matematicas xD")

if __name__ == "__main__":
    print("Este código se ejecuta solo si el script se ejecuta directamente.")


